    function parsePriceNumber(label) {
      if (!label) return null;

      const text = String(label).replace(/\s/g, "").replace(",", ".");
      const match = text.match(/(\d+(?:\.\d+)?)/);

      if (!match) return null;
      const value = Number(match[1]);

      return Number.isFinite(value) ? value : null;
    }

    function getPriceEntries(game) {
      const prices = game.data && game.data.prices ? game.data.prices : {};
      const entries = [];

      if (prices.steam) entries.push(prices.steam);
      if (prices.epic) entries.push(prices.epic);
      if (!entries.length && prices.USD) entries.push(prices);

      return entries;
    }

