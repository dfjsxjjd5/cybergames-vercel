    function priceMatches(game, value) {
      if (value === "all") return true;

      if (value === "unavailable") {
        if (game.removed || game.status === "failed") return false;
        const prices = getUsdPrices(game);
        return game.data && (!prices.length || prices.every((price) => price.unavailable || price.label === "Уточнить" || price.label === "Недоступно"));
      }

      if (!game.data) return false;

      if (value === "free") return gameIsFree(game);
      if (value === "sale") return gameHasDiscount(game);

      const minUsd = gameMinUsd(game);
      if (!Number.isFinite(minUsd)) return false;

      if (value === "under-5") return minUsd <= 5;
      if (value === "under-10") return minUsd <= 10;
      if (value === "under-30") return minUsd <= 30;

      return true;
    }


    function catalogCandidates() {
      const orderMap = new Map(FEATURED_ORDER.map((title, index) => [normalize(title), index]));
      return games
        .filter((game) => (Boolean(game.appId) || gamePlatformValue(game) !== "none") && !game.removed)
        .sort((a, b) => {
          const aOrder = orderMap.has(normalize(a.title)) ? orderMap.get(normalize(a.title)) : 9999;
          const bOrder = orderMap.has(normalize(b.title)) ? orderMap.get(normalize(b.title)) : 9999;
          if (aOrder !== bOrder) return aOrder - bOrder;
          return a.title.localeCompare(b.title, "ru");
        });
    }

