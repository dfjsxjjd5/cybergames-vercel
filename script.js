    function updateCard(game) {
      const card = getCard(game);
      if (!card) return;

      card.dataset.search = game.searchText || normalize(game.title);

      const poster = card.querySelector(".poster");
      const title = card.querySelector(".card-title");
      const release = card.querySelector(".release");
      const type = card.querySelector(".type");
      const desc = card.querySelector(".card-description");
      const status = card.querySelector(".status-pill");
      const freeBadge = card.querySelector(".free-badge");
      const tagRow = card.querySelector(".tag-row");
      const price = card.querySelector(".card-price");
      const storeBadge = card.querySelector(".store-badge");
      const storeMini = card.querySelector(".store-mini");

      if (game.status === "loading") {
        status.textContent = "Загрузка...";
        status.className = "status-pill";
        if (price) price.textContent = "Цена загружается";
      }

      if (game.status === "failed") {
        poster.src = createPlaceholder(game.title);
        title.textContent = game.title;
        release.textContent = "—";
        type.textContent = "нет Steam/Epic data";
        desc.textContent = game.error || "Steam/Epic не нашли данные. Карточка будет скрыта из каталога.";
        status.textContent = "Нет данных";
        status.className = "status-pill fail";
        freeBadge.classList.remove("show");
        tagRow.innerHTML = `<span class="tag">Скрыто</span><span class="tag">Нет Steam/Epic data</span>`;
        if (price) price.textContent = "Нет цены";
      }

      if (game.status === "done" && game.data) {
        const data = game.data;
        const priceInfo = bestPriceInfo(game);
        poster.src = cardImage(game);
        poster.onerror = () => {
          poster.onerror = null;
          poster.src = createPlaceholder(game.title);
        };

        title.textContent = data.title || game.title;
        release.textContent = data.releaseDate || "—";
        type.textContent = data.type || "game";
        desc.textContent = data.description || "Описание не найдено.";
        status.textContent = gamePlatformLabel(game);
        status.className = "status-pill ok";

        if (data.isFree) freeBadge.classList.add("show");
        else freeBadge.classList.remove("show");

        const tags = gameTags(data, game).slice(0, 3).map((tag) => `<span class="tag">${escapeHtml(tag)}</span>`).join("");
        tagRow.innerHTML = `${data.isFree ? '<span class="tag">Бесплатно</span>' : ""}<span class="tag">${escapeHtml(gamePlatformLabel(game))}</span>${tags}`;
        if (price) price.textContent = priceInfo.label;
        if (storeBadge) storeBadge.textContent = priceInfo.provider || gamePlatformLabel(game);
        if (storeMini) storeMini.textContent = gamePlatformLabel(game, true);
      }

      if (currentDetailId === game.id) renderDetailPanel(game);
    }

