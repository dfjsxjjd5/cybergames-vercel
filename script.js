    async function fetchGame(game) {
      if (game.status === "loading" || game.status === "done" || game.status === "failed") return game.data || null;

      const cached = getCached(game);

      if (cached) {
        applyGameData(game, cached);
        return cached;
      }

      game.status = "loading";
      updateCard(game);

      const params = new URLSearchParams();
      params.set("title", game.title);
      if (game.appId) params.set("appId", String(game.appId));

      try {
        const response = await fetch(`/api/steam?${params.toString()}`);
        const data = await response.json();

        if (!response.ok || !data.ok) {
          game.status = "failed";
          game.error = data.reason || data.error || "Steam/Epic не нашли данные";
          game.data = data;
          if (currentModalId === game.id) renderModal(game);
          removeNoStoreGame(game);
          return data;
        }

        setCached(game, data);
        applyGameData(game, data);
        return data;
      } catch (error) {
        game.status = "failed";
        game.error = error.message || "Ошибка API";
        if (currentModalId === game.id) renderModal(game);
        removeNoStoreGame(game);
        return null;
      }
    }


    function uniqueList(values) {
      const seen = new Set();
      const result = [];

      values
        .filter(Boolean)
        .map((item) => String(item).trim())
        .filter(Boolean)
        .forEach((item) => {
          const key = normalize(item);
          if (!key || seen.has(key)) return;
          seen.add(key);
          result.push(item);
        });

      return result;
    }

