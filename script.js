    async function loadAll() {
      loadAllBtn.disabled = true;
      loadAllBtn.textContent = "Загружаю...";
      progress.classList.add("show");
      progressFill.style.width = "0%";

      let done = 0;
      const queue = catalogCandidates().filter((game) => getCard(game));
      const totalQueue = queue.length;
      const workers = 4;

      async function worker() {
        while (queue.length) {
          const game = queue.shift();
          await fetchGame(game);
          done++;
          progressFill.style.width = `${Math.round((done / Math.max(totalQueue, 1)) * 100)}%`;
        }
      }

      await Promise.all(Array.from({ length: workers }, worker));

      loadAllBtn.disabled = false;
      loadAllBtn.textContent = "Обновить каталог";
      setTimeout(() => progress.classList.remove("show"), 1200);
    }

